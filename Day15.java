public class Day15 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Penjumlahan
        int hasilTambah = a + b;
        System.out.println(a + " + " + b + " = " + hasilTambah);

        // Pengurangan
        int hasilKurang = a - b;
        System.out.println(a + " - " + b + " = " + hasilKurang);
    }
}
